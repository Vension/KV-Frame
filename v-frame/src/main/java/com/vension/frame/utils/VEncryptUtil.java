	/**
	 * 描述：MD5加密.
	 *
	 * @param str 要加密的字符串
	 * @return String 加密的字符串
	 */
	public final static String MD5(String str) {
		try {
			byte[] strTemp = str.getBytes();
			// 获得MD5摘要算法的 MessageDigest 对象
			MessageDigest mdTemp = MessageDigest.getInstance("MD5");
			mdTemp.update(strTemp); // 使用指定的字节更新摘要
			byte tmp[] = mdTemp.digest(); // 获得密文 MD5 的计算结果是一个 128 位的长整数，
			// 用字节表示就是 16 个字节
			// 把密文转换成十六进制的字符串形式
			int j = tmp.length;
			char strs[] = new char[j * 2]; // 每个字节用 16 进制表示的话，使用两个字符，
			// 所以表示成 16 进制需要 32 个字符
			int k = 0; // 表示转换结果中对应的字符位置
			for (int i = 0; i < j; i++) { // 从第一个字节开始，对 MD5 的每一个字节
				// 转换成 16 进制字符的转换
				byte byte0 = tmp[i]; // 取第 i 个字节
				strs[k++] = hexDigits[byte0 >>> 4 & 0xf]; // 取字节中高 4 位的数字转换,
				// >>> 为逻辑右移，将符号位一起右移
				strs[k++] = hexDigits[byte0 & 0xf]; // 取字节中低 4 位的数字转换
			}
			return new String(strs).toLowerCase(); // 换后的结果转换为字符串
		} catch (Exception e) {
			return null;
		}
	}

	/**
	 * AES加密
	 *
	 * @param content 明文
	 * @param pass  秘钥
	 * @return 密文
	 */
	public static String encryptAES(String content, String pass) throws Exception {
		Cipher aesECB = Cipher.getInstance(AES_MODE);
		SecretKeySpec key = getSecretKeySpec(pass);
		aesECB.init(Cipher.ENCRYPT_MODE, key);
		byte[] result = aesECB.doFinal(content.getBytes());
		return parseByte2HexStr(result);
	}

    private static final String ALGORITHM = "AES";
    private static final String TRANSFORMATION = "AES/ECB/PKCS5Padding";

    /**
     * 加密
     *
     * @param  input 加密的字符串
     * @param  key   解密的key
     * @return HexString
     */

    public static String encrypt(String input, String key){
        byte[] crypted = null;
        try{
            SecretKeySpec skey = new SecretKeySpec(key.getBytes(), ALGORITHM);
            Cipher cipher = Cipher.getInstance(TRANSFORMATION);
            cipher.init(Cipher.ENCRYPT_MODE, skey);
            crypted = cipher.doFinal(input.getBytes());
        }catch(Exception e){
            System.out.println(e.toString());
        }

        return parseByte2HexStr(crypted);
    }

    /**
     * 将二进制转换成16进制
     * @param buf
     * @return
     */
    private static String parseByte2HexStr(byte buf[]) {
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < buf.length; i++) {
            String hex = Integer.toHexString(buf[i] & 0xFF);
            if (hex.length() == 1) {
                hex = '0' + hex;
            }
            sb.append(hex.toUpperCase());
        }
        return sb.toString();
    }


	/**
	 * 将16进制转换为二进制
	 *
	 * @param hexStr
	 * @return
	 */
	private static byte[] parseHexStr2Byte(String hexStr) {
		if (hexStr.length() < 1)
			return null;
		byte[] result = new byte[hexStr.length() / 2];
		for (int i = 0; i < hexStr.length() / 2; i++) {
			int high = Integer.parseInt(hexStr.substring(i * 2, i * 2 + 1), 16);
			int low = Integer.parseInt(hexStr.substring(i * 2 + 1, i * 2 + 2),16);
			result[i] = (byte) (high * 16 + low);
		}
		return result;
	}

    /**
     * 解密
     *
     * @param  input 解密的字符串
     * @param  key   解密的key
     * @return String
     */
    public static String decrypt(String input, String key) {
        byte[] output = null;
        try {
            SecretKeySpec skey = new SecretKeySpec(key.getBytes(), ALGORITHM);
            Cipher cipher = Cipher.getInstance(TRANSFORMATION);
            cipher.init(Cipher.DECRYPT_MODE, skey);
            output = cipher.doFinal(parseHexStr2Byte(input));

        } catch (Exception e) {
            Log.e("AES", "解密异常：" + e.toString());
        }
        return new String(output);
    }


	/**
	 * AES解密
	 *
	 * @param content 密文
	 * @param pass  秘钥
	 * @return 明文
	 */
	public static String decryptAES(String content, String pass) throws Exception {
		Cipher cipher = Cipher.getInstance(AES_MODE);
		SecretKeySpec key = getSecretKeySpec(pass);
		cipher.init(Cipher.DECRYPT_MODE, key);
		byte[] result = parseHexStr2Byte(content);
		return new String(cipher.doFinal(result));
	}

	private static SecretKeySpec getSecretKeySpec(String key) throws Exception {
		byte[] arrBTmp = key.getBytes();
		byte[] arrB = new byte[16];
		for (int i = 0; i < arrBTmp.length && i < arrB.length; i++) {
			arrB[i] = arrBTmp[i];
		}
		return new SecretKeySpec(arrB, AES_Algorithm);
	}

